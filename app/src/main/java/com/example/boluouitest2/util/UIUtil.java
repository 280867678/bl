package com.example.boluouitest2.util;

import android.content.Context;

public class UIUtil {
    public static int m8988a(Context context, double d) {
        double d2 = context.getResources().getDisplayMetrics().density;
        Double.isNaN(d2);
        return (int) ((d * d2) + 0.5d);
    }


}
